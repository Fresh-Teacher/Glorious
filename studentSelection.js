const classSelect = document.getElementById('classSelect');
const streamSelect = document.getElementById('streamSelect');
const nameSelect = document.getElementById('nameSelect');
const startExamButton = document.getElementById('startExam');

// Populate class select
for (const className in studentData) {
    const option = document.createElement('option');
    option.value = className;
    option.textContent = className;
    classSelect.appendChild(option);
}

classSelect.addEventListener('change', function() {
    streamSelect.innerHTML = '<option value="">Select Stream</option>';
    nameSelect.innerHTML = '<option value="">Select Name</option>';
    streamSelect.disabled = false;
    nameSelect.disabled = true;
    startExamButton.disabled = true;

    if (this.value) {
        for (const streamName in studentData[this.value]) {
            const option = document.createElement('option');
            option.value = streamName;
            option.textContent = streamName;
            streamSelect.appendChild(option);
        }
    }
});

streamSelect.addEventListener('change', function() {
    nameSelect.innerHTML = '<option value="">Select Name</option>';
    nameSelect.disabled = false;
    startExamButton.disabled = true;

    if (this.value) {
        const names = studentData[classSelect.value][this.value];
        names.forEach(name => {
            const option = document.createElement('option');
            option.value = name;
            option.textContent = name;
            nameSelect.appendChild(option);
        });
    }
});

nameSelect.addEventListener('change', function() {
    startExamButton.disabled = !this.value;
});

startExamButton.addEventListener('click', function() {
    const studentInfo = {
        name: nameSelect.value,
        class: classSelect.value,
        stream: streamSelect.value,
        gender: streamSelect.value.includes('Girls') ? 'Female' : 'Male'
    };
    localStorage.setItem('studentInfo', JSON.stringify(studentInfo));
    window.location.href = 'exam.html'; // Redirect to the exam page
});